import Foundation
import UIKit

// 다자이너 Zepplin 색상 가이드 Extension

extension UIColor {
    
    // zepplin으로 색상 따온 코드 넣기
    @nonobjc class var lightGreenishBlue: UIColor {
      return UIColor(red: 114.0 / 255.0, green: 235.0 / 255.0, blue: 200.0 / 255.0, alpha: 1.0)
    }
}
